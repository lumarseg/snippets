# booleans
print("* Boolean")
is_single = True
print("is_single =>", type(is_single))
print(is_single)
is_single = False
print(is_single)
print("")

# invert value
print("* Invert value")
print("True  =>", not True)
print("False =>", not False)
print("---")
is_single = True
print("is_single =", is_single)
print(not is_single)
is_single = not is_single
print(is_single)